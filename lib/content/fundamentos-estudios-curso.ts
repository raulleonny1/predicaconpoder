export function getFundamentosChapter(slug: string) {
  return FUNDAMENTOS_CURSO_CHAPTERS.find((c) => c.slug === slug) ?? null;
}

export function getFundamentosNeighbors(order: number) {
  const sorted = [...FUNDAMENTOS_CURSO_CHAPTERS].sort((a, b) => a.order - b.order);
  const idx = sorted.findIndex((c) => c.order === order);
  if (idx === -1) return { prev: null as FundamentosChapter | null, next: null as FundamentosChapter | null };
  return {
    prev: sorted[idx - 1] ?? null,
    next: sorted[idx + 1] ?? null,
  };
}

export function groupFundamentosByModule(chapters: FundamentosChapter[]) {
  const map = new Map<string, { moduleNumber: number; moduleTitle: string; items: FundamentosChapter[] }>();
  for (const ch of [...chapters].sort((a, b) => a.order - b.order)) {
    const key = `${ch.moduleNumber}::${ch.moduleTitle}`;
    if (!map.has(key)) {
      map.set(key, { moduleNumber: ch.moduleNumber, moduleTitle: ch.moduleTitle, items: [] });
    }
    map.get(key)!.items.push(ch);
  }
  return Array.from(map.values()).sort((a, b) => a.moduleNumber - b.moduleNumber);
}
